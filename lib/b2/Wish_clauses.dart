İngilizcede “keşke” anlamını vermek için “if only” ve “wish” kalıpları kullanılır. “If only” kalıbının verdiği anlam daha güçlü olduğu için genellikle günlük hayatta “wish” kalıplarından yararlanılır. “If only” kalıbının değişik zaman kipleriyle kullanımını aşağıdaki örneklerle inceleyelim.

Past Tense + If Only
Cümlenin gramatik yapısı geçmiş zamana ait olsa da past tense ve if only kalıpları bir araya geldiğinde geçmişteki bir olaydan bahsetmez, şimdiki zamanda gerçekleşen bir pişmanlık durumundan bahseder. Örneğin:

If only that bag wasn’t this expensive, I would buy it.
(Keşke o çanta bu kadar pahalı olmasaydı, alırdım.)

If only I slept earlier yesterday, I wouldn’t be tired right now.
(Keşke dün daha erken yatsaydım, şu anda yorgun olmazdım.)

If only you knew the truth.
(Keşke gerçeği bilseydin.)

Past Perfect Tense + If Only
If only kalıbı Past Perfect Tense ile kullanıldığında geçmişte yaşanan olaylara karşı duyulan pişmanlıkları anlatmak için kullanılır. Örneğin:

If only you had been more careful, the vase wouldn’t break.
(Keşke daha dikkatli olsaydın, vazo kırılmazdı.)

If only we had been there, we would help you.
(Keşke biz de orada olsaydık, sana yardım ederdik.)

If only she hadn’t watched that film, we could watch it together.
