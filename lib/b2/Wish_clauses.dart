Wish Clause Gramer Formülü Nedir?

Wish clause cümle içinde kullanılan zamire ve cümlede verilmek istenilen anlama göre formülünde değişiklik gösteren bir konudur. Çeşitli durumlarda ve çeşitli zamirlerde wish clause yapısının nasıl değiştiğini ve nasıl kullanılacağını yukarıda bulabilirsiniz. Özet olarak wish clause kalıbının gramatik yapısı aşağıdaki gibidir:

wish + past simple
wish + past continuous
wish + past perfect
wish + would + bare infinitive
wish + infinitive or wish + object + infinitive

